package io.quarkus.bot.util;

public class Strings {
    public static final String EDITORIAL_RULES_COMMENT_MARKER = "\n<!-- Quarkus-Bot-Editor-Rules -->";

    public static boolean isNotBlank(String string) {
        return string != null && !string.trim().isEmpty();
    }

    public static boolean isBlank(String string) {
        return string == null || string.trim().isEmpty();
    }

    public static String commentByBot(String originalComment) {
        StringBuilder sb = new StringBuilder(originalComment);
        if (!originalComment.endsWith("\n")) {
            sb.append("\n");
        }
        sb.append("\n<sub>This message is automatically generated by a bot.</sub>");
        return sb.toString();
    }

    public static String editorialCommentByBot(String originalComment) {
        return commentByBot(originalComment) + EDITORIAL_RULES_COMMENT_MARKER;
    }

    private Strings() {
    }
}
